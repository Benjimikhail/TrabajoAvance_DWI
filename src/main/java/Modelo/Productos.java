
package Modelo;

public class Productos {
        int ProdID;
        String NombreP;
        String DescripP;
        double Precio;
        int CateP;
        int ProveP;
        String imagen;

    public String getImagen() {
        return imagen;
    }

    public void setImagen(String imagen) {
        this.imagen = imagen;
    }

    public int getProdID() {
        return ProdID;
    }

    public void setProdID(int ProdID) {
        this.ProdID = ProdID;
    }

    public String getNombreP() {
        return NombreP;
    }

    public void setNombreP(String NombreP) {
        this.NombreP = NombreP;
    }

    public String getDescripP() {
        return DescripP;
    }

    public void setDescripP(String DescripP) {
        this.DescripP = DescripP;
    }

    public double getPrecio() {
        return Precio;
    }

    public void setPrecio(double Precio) {
        this.Precio = Precio;
    }

    public int getCateP() {
        return CateP;
    }

    public void setCateP(int CateP) {
        this.CateP = CateP;
    }

    public int getProveP() {
        return ProveP;
    }

    public void setProveP(int ProveP) {
        this.ProveP = ProveP;
    }
        
        
}
